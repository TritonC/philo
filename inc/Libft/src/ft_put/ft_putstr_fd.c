#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	i;

	if (s != NULL)
	{	
		i = 0;
		if (*(s + i) != '\0')
		{
			while (*(s + i) != '\0')
			{
				ft_putchar_fd(*(s + i), fd);
				i++;
			}
		}
	}	
}
