#include "libft.h"

size_t	ft_strlcpy(char *d, char *s, size_t size)
{
	size_t	i;

	i = 0;
	if (size != 0)
	{	
		while (*(s + i) != '\0' && i < size - 1)
		{
			*(d + i) = *(s + i);
			i++;
		}
		*(d + i) = '\0';
	}
	return (ft_strlen(s));
}
