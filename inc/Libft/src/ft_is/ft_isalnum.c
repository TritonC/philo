int     ft_isalnum(int n)
{
        int     value;

        value = 0;
        if ((n >= '0' && n <= '9') || (n >= 'a' && n <= 'z')
                || (n >= 'A' && n <= 'Z'))
                value = 1;
        return (value);
}
